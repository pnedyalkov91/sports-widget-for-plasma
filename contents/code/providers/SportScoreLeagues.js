function countryOptions() {
    return FOOTBALL_COUNTRIES.map(country => ({ label: country.label, value: country.value, icon: flagSource(country.value) }));
}

function leagueOptions(countryCode) {
    const country = countryByCode(countryCode);
    return country ? country.leagues.map(league => ({ label: league.label, value: league.value })) : [];
}

function leagueLabel(leagueCode) {
    const value = String(leagueCode || "").trim();
    if (value.length === 0)
        return "";

    for (let countryIndex = 0; countryIndex < FOOTBALL_COUNTRIES.length; countryIndex += 1) {
        const leagues = FOOTBALL_COUNTRIES[countryIndex].leagues;
        for (let leagueIndex = 0; leagueIndex < leagues.length; leagueIndex += 1) {
            if (leagues[leagueIndex].value === value)
                return leagues[leagueIndex].label;
        }
    }
    return "";
}

function countryByCode(countryCode) {
    const value = String(countryCode || "world").trim().toLowerCase();
    for (let index = 0; index < FOOTBALL_COUNTRIES.length; index += 1) {
        if (FOOTBALL_COUNTRIES[index].value === value)
            return FOOTBALL_COUNTRIES[index];
    }
    return FOOTBALL_COUNTRIES[0];
}

function flagSource(countryCode) {
    const code = COUNTRY_FLAG_CODES[String(countryCode || "").trim().toLowerCase()] || "";
    if (code.length === 0)
        return countryCode === "world" ? "globe" : "";

    return `file:///usr/share/locale/l10n/${code}/flag.png`;
}
